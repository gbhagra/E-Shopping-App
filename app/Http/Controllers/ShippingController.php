     */
    public function store(Request $request)
    {
        //
       
        Shipping::create([
            'user_id' => auth()->user()->id,
            'name' => $request->name,
            'address' => $request->address ." ".$request->pincode ." ". $request->state,
            'phone' => $request->phone
        ]);
        
        return redirect()->home();
    }

